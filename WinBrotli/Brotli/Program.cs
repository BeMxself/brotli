using System;
using System.Diagnostics;

namespace Brotli
{
    class Program
    {
        static void Main(string[] args)
        {
            if (Debugger.IsAttached)
            {
                SafeMain(args);
            }
            else
            {
                try
                {
                    SafeMain(args);
                }
                catch (Exception e)
                {
                    Console.WriteLine(e);
                }
            }
        }

        static void SafeMain(string[] args)
        {
            BrotliCompression.Decompress("alice29.txt.compressed", "alice29.txt");
        }
    }
}
